    public int nhapDataVaoSQL(int x)
    {
        int kq=0;
        int kt;
        if(x!=0)
        {
            for(int i=0;i<table_kq.getRowCount();i++)
            {
                String maKD = (String) table_kq.getValueAt(i, 0);
                String chuSH = (String) table_kq.getValueAt(i, 1);
                String maDA = (String) table_kq.getValueAt(i, 2);
                kt=kiemTraMDAExists(maKD);
                if(kt==0)
                {
                    String sql="INSERT INTO KHUDAT VALUES(?,?,?)";
                    try{
                        PreparedStatement ps=conn.prepareStatement(sql);
                        ps.setString(1, maKD);
                        ps.setString(2, chuSH);
                        ps.setString(3,maDA);

                        ps.executeUpdate();
                        ps.close();
                        kq++;
                    }
                    catch(Exception e)
                    {
                        e.printStackTrace(); 
                    }
                }
                
                
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Không có dữ liệu mới !");
        }
                
        return kq;
    }
    
