    public void docdata()
    {
        String sql="SELECT * FROM KHUDAT";
        try{
            PreparedStatement ps=conn.prepareStatement(sql);
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                String maKD=rs.getString(1);
                String chuSH=rs.getString(2);
                String maDA=rs.getString(3);
                
                
                Vector r=new Vector();
                r.add(maKD);
                r.add(chuSH);
                r.add(maDA);
                
                nd.add(r);
                table_kq.setModel(new DefaultTableModel(nd,td));
            }
            rs.close();
            ps.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Doc du lieu loi !");
            e.printStackTrace();
        }
        
    }
    
