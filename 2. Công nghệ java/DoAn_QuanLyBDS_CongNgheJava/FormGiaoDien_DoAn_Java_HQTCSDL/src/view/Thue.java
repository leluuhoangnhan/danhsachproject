    public void docdata()
    {
        String sql="SELECT * FROM THUE";
        try{
            PreparedStatement ps=conn.prepareStatement(sql);
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                String maThue=rs.getString(1);
                String tenThue=rs.getString(2);
                String loaiThue=rs.getString(3);
                
                
                Vector r=new Vector();
                r.add(maThue);
                r.add(tenThue);
                r.add(loaiThue);
                
                nd.add(r);
                table_kq.setModel(new DefaultTableModel(nd,td));
            }
            rs.close();
            ps.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Doc du lieu loi !");
            e.printStackTrace();
        }
        
    }
    
