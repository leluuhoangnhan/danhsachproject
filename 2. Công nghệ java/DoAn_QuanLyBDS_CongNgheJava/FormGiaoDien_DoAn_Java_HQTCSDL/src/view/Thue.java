    public int nhapDataVaoSQL(int x)
    {
        int kq=0;
        int kt;
        if(x!=0)
        {
            for(int i=0;i<table_kq.getRowCount();i++)
            {
                String maThue = (String) table_kq.getValueAt(i, 0);
                String tenThue = (String) table_kq.getValueAt(i, 1);
                String loaiThue = (String) table_kq.getValueAt(i, 2);
                kt=kiemTraMDAExists(maThue);
                if(kt==0)
                {
                    String sql="INSERT INTO THUE VALUES(?,?,?)";
                    try{
                        PreparedStatement ps=conn.prepareStatement(sql);
                        ps.setString(1, maThue);
                        ps.setString(2, tenThue);
                        ps.setString(3,loaiThue);

                        ps.executeUpdate();
                        ps.close();
                        kq++;
                    }
                    catch(Exception e)
                    {
                        e.printStackTrace(); 
                    }
                }
                
                
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Không có dữ liệu mới !");
        }
                
        return kq;
    }
    
