    public int timKiem(String maDA)
    {
        int kt=1;
        String sql1="SELECT * FROM DUAN";
        try{
            PreparedStatement ps1=conn.prepareStatement(sql1);
            ResultSet rs=ps1.executeQuery();
            while(rs.next())
            {
                if(maDA.equals(rs.getString("MADUAN")))
                {
                   return kt; 
                }
                kt++;
            }
            rs.close();
            ps1.close();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        return 0;
    }
    
    public int nhapFileThemMoi(String url)
    {
        int kq=0;
        try{
            File f = new File(url);
            if (f.exists())
            {
                try(BufferedReader br=new BufferedReader(new FileReader(f)))
                {
                    String value;
                    while((value=br.readLine())!=null)
                    {
                        StringTokenizer stk=new StringTokenizer(value,";");
                        String maDA=stk.nextToken();
                        String tenDA=stk.nextToken();
                        String diaChi=stk.nextToken();
                        String namBG=String.valueOf(stk.nextToken());
                        String maKD=stk.nextToken();
                        String soLuongKH=String.valueOf(stk.nextToken());
                        String soLuongCDT=String.valueOf(stk.nextToken());
                        Vector r=new Vector();
                        r.add(maDA);
                        r.add(tenDA);
                        r.add(diaChi);
                        r.add(namBG);
                        r.add(maKD);
                        r.add(soLuongKH);
                        r.add(soLuongCDT);
                        nd.add(r);
                        kq++;
                    }
                }
            }
            
            return kq;
        }
        catch(Exception e){
            e.printStackTrace();
            return kq;
        }
    }
    
