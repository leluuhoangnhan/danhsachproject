    public void docdata()
    {
        String sql="SELECT * FROM DUAN";
        try{
            PreparedStatement ps=conn.prepareStatement(sql);
            ResultSet rs=ps.executeQuery();
            while(rs.next())
            {
                String maDA=rs.getString(1);
                String tenDA=rs.getString(2);
                String diaChi=rs.getString(3);
                int namBG=rs.getInt(4);
                String maKD=rs.getString(5);
                int soLuongKH=rs.getInt(6);
                int soLuongCDT=rs.getInt(7);
                
                
                
                Vector r=new Vector();
                r.add(maDA);
                r.add(tenDA);
                r.add(diaChi);
                r.add(namBG);
                r.add(maKD);
                r.add(soLuongKH);
                r.add(soLuongCDT);
                
                nd.add(r);
                table_kq.setModel(new DefaultTableModel(nd,td));
            }
            rs.close();
            ps.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Doc du lieu loi !");
            e.printStackTrace();
        }
        
    }
    
