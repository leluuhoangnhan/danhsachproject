    public int kt_maKH_HopLe(String maKH)
    {
        for(int i=0;i<maKH.length();i++)
        {
            char c=maKH.charAt(i);
            if(i==0 || i==1)
            {
                if(i==0)
                {
                    if(c!='K')
                        return 0;
                }
                else
                {
                    if(c!='H')
                        return 0;
                }   
            }
            else
            {
                if(c<'0'||c>'9')
                    return 0;
            }
        }
        return 1;
    }
    
