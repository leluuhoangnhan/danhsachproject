    public int nhapFileMoi(String url)
    {
        int kq=0;
        try{
            File f = new File(url);
            if(f.exists())
            {
                try(BufferedReader br=new BufferedReader(new FileReader(f)))
                {
                    String value;
                    while((value=br.readLine())!=null)
                    {
                        StringTokenizer stk=new StringTokenizer(value,";");
                        String maKH=stk.nextToken();
                        String tenKH=stk.nextToken();
                        String ngaySinh=stk.nextToken();
                        String gioiTinh=stk.nextToken();
                        String ngayMua_Thue=stk.nextToken();
                        String maDuAn=stk.nextToken();

                        Vector r=new Vector();
                        r.add(maKH);
                        r.add(tenKH);
                        r.add(ngaySinh);
                        r.add(gioiTinh);
                        r.add(ngayMua_Thue);
                        r.add(maDuAn);
                        nd.add(r);
                        kq++;
                    }
                }
                catch(Exception ex)
                {
                    ex.printStackTrace();
                }
            }
            return kq;
        }
        catch(Exception e){
            e.printStackTrace();
            return kq;
        }
    }
    
